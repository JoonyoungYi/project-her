public class PronounciationAlphbetManager {

    public static String getPronounciationOfAlphabet(char c) {
        if (c == 'a') {
            return "에이";
        } else if (c == 'b') {
            return "비";
        } else if (c == 'c') {
            return "씨";
        } else if (c == 'd') {
            return "디";
        } else if (c == 'e') {
            return "이";
        } else if (c == 'f') {
            return "에프";
        } else if (c == 'g') {
            return "지";
        } else if (c == 'h') {
            return "에이치";
        } else if (c == 'i') {
            return "아이";
        } else if (c == 'j') {
            return "제이";
        } else if (c == 'k') {
            return "케이";
        } else if (c == 'l') {
            return "엘";
        } else if (c == 'm') {
            return "엠";
        } else if (c == 'n') {
            return "엔";
        } else if (c == 'o') {
            return "오";
        } else if (c == 'p') {
            return "피";
        } else if (c == 'q') {
            return "큐";
        } else if (c == 'r') {
            return "얼";
        } else if (c == 's') {
            return "에스";
        } else if (c == 't') {
            return "티";
        } else if (c == 'u') {
            return "유";
        } else if (c == 'v') {
            return "브이";
        } else if (c == 'w') {
            return "더블유";
        } else if (c == 'x') {
            return "엑스";
        } else if (c == 'y') {
            return "와이";
        } else if (c == 'z') {
            return "지";
        }
        return null;
    }
}
